console.log("***** Swapping of Two Variables*****");

console.log("-----Before Swapping-----");
var sweety="sweety"
console.log(sweety);
var cutie="cutie"
console.log(cutie);
console.log("-----After Swapping-----");
var temp=sweety;
sweety=cutie;
console.log(sweety);
cutie=temp;
console.log(cutie);

